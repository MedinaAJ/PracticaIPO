    private boolean ComprobarDatos() {
        err1 = "";
        user = txtUsuario.getText();
        pass = txtPass.getText();
        boolean comprobar = true;          
        if(user.equals("")){
            err1 = err1 + errUser + "\n";
            comprobar = false;
        }
        if(pass.equals("")){
            err1 = err1 + errPass + "\n";
            comprobar = false;
        }
        if(!comprobar){
            JOptionPane.showMessageDialog(null, err1);
        }
        return comprobar;
    }
    
