    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:
        java.util.Date fecha = new Date();
        Format formatter = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String ultimoAcceso = formatter.format(fecha);
        if(!rutaBBDD.equals("")){
            c = new Conectar(rutaBBDD);
            c.PrepararBaseDatos();
        if(ComprobarDatos()){
         if(c.verificarContraseña(user, pass) && !rutaBBDD.equals("")){
             this.setVisible(false);
             c.modificarFechaAcceso(user, ultimoAcceso);
             c.desconectar();
             MenuPrincipal p = new MenuPrincipal(user, pass, rutaBBDD, idioma.getIdioma());
             if(idioma.getIdioma().equals("Ingles"))
                 p.setTitle("Main");            
             p.setVisible(true);
             
         }else if(!c.verificarContraseña(user, pass)){
             JOptionPane.showMessageDialog(null, m1);
         }
         
        }}else{
            JOptionPane.showMessageDialog(null, m2);
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

