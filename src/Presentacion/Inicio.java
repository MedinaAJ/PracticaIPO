    public void cambiarIdioma(String nombreIdioma){
        
        idioma=new Idioma(nombreIdioma);
        
        lblLenguaje.setText(idioma.getProperty("tituloSL"));
        lblSelectBD.setText(idioma.getProperty("lblSelectBD"));
        btnEntrar.setText(idioma.getProperty("btnEntrar"));
        btnSalir.setText(idioma.getProperty("btnSalir")); 
        btnBuscar.setText(idioma.getProperty("btnBuscar"));
        lbluser.setText(idioma.getProperty("IntUser"));
        lblpass.setText(idioma.getProperty("IntPass"));
        m1 = idioma.getProperty("m1");
        m2 = idioma.getProperty("m2");
        errPass = idioma.getProperty("errPass");
        errUser = idioma.getProperty("errUser");
        cmbIdiomas.removeAllItems();
        
        String idiomas[]={
                          idioma.getProperty("español"),
                          idioma.getProperty("ingles")
                          };
        
        for(int i=0;i<idiomas.length;i++){
            cmbIdiomas.addItem(idiomas[i]);
        }
        
    }

